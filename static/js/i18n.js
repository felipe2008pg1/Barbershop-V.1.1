const I18N_STORAGE_KEY = "barbershop_lang";
const DEFAULT_LANG = "pt";

function getCurrentLang() {
  return window.__barbershopLang || DEFAULT_LANG;
}

function t(key) {
  const lang = getCurrentLang();
  return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || key;
}

function applyTranslations() {
  const lang = getCurrentLang();
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.setAttribute("placeholder", t(el.getAttribute("data-i18n-placeholder")));
  });
  document.documentElement.lang = lang === "pt" ? "pt-BR" : "en";
  const toggleLabel = document.querySelector("[data-i18n-toggle-label]");
  if (toggleLabel) toggleLabel.textContent = t("lang_toggle_label");
}

function setLang(lang) {
  if (lang !== "pt" && lang !== "en") return;
  window.__barbershopLang = lang;
  localStorage.setItem(I18N_STORAGE_KEY, lang);
  applyTranslations();
  document.dispatchEvent(new CustomEvent("langchange", { detail: { lang } }));
}

function toggleLang() {
  setLang(getCurrentLang() === "pt" ? "en" : "pt");
}

function initI18n() {
  const saved = localStorage.getItem(I18N_STORAGE_KEY);
  window.__barbershopLang = saved === "en" || saved === "pt" ? saved : DEFAULT_LANG;
  applyTranslations();
}

document.addEventListener("DOMContentLoaded", initI18n);
